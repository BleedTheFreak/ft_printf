#include "ft_printf.h"

int	ft_len(unsigned long nbr, int base)
{
	int				i;

	i = 0;
	if (nbr == 0)
		return (1);
	if (nbr < 0)
	{
		nbr = nbr * -1;
	}
	while (nbr > 0)
	{
		i++;
		nbr /= base;
	}
	return (i);
}
